                            <p><b>Em dic Arnald SAXONARA, sóc músic i saxofonista en esdeveniments, bodes i també fent concerts, a continuació us faig un breu resum de la meva carrera</b></p>
                            <p>Vaig néixer el 1990 👶 i vaig començar els meus estudis de música amb 7 anys, inicialment tocant el <b>piano </b>🎹 i seguint la formació reglada de les Escoles de Música de Linyola i Mollerussa. Amb el pas del temps, i segons van anar evolucionant els meus gustos, vaig deixar de banda el piano per centrar-me en l'estudi de la <b>música moderna</b>, aquesta vegada com a <b>guitarrista 🎸</b> i ja participant de bandes i tocant en directe. Uns anys després, ja d'adolescent, m'inicio amb el <b>saxofon 🎷</b> com a segon instrument, però ràpidament pren tot el protagonisme, relegant la guitarra a un segon pla. És amb el saxofon amb què curso al <b>Conservatori de Cervera</b>.</p>
                            <p>Juntament amb els meus estudis de música també em vaig graduar a <b>Administració i Direcció d'Empreses 📈</b> per la Universitat de Lleida i a Centre College, Kentucky, <b>Estats Units 🇺🇸</b>, on vaig estudiar una barreja de matèries en ensenyament liberal que van des del Jazz i la Música fins a les Finances i l'Economia.</p>
                            <p>El 2013, amb el retorn <b>dels Estats Units a Catalunya</b>, comença un temps de readaptació, on m'incorporo com a saxofonista en bandes de funk-disco, de rockabilly i de jazz, sempre inquiet per tocar i aprendre dels diferents estils, i buscant la manera de presentar els projectes en concerts i esdeveniments en directe. En aquest temps segueixo formant-me també com a músic a acadèmies.</p>
                            <p>Ja al 2015 em mudo a <b>Puerto Rico 🇵🇷</b>, establint-me a la seva capital, San Juan. És allà on començo a familiaritzar-me amb tota la música tradicional de l'illa, especialment la Salsa, Bomba, Rumba i Plena. A Puerto Rico vaig formar la meva agrupació amb músics del <b>Conservatori de Música de Puerto Rico</b>, actuant en llocs tan icònics com l'escenari del barri de La Perla, al Viejo San Juan, el mateix escenari on va actuar la mítica banda Puertorriqueña Calle 13 després de ser guardonats amb premis Grammy Llatí.</p>
