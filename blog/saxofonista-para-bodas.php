                                        <h2>Música para tu boda</h2>
                                        <p>El saxofón es un instrumento de los más versátiles, pudiendo ofrecer un sonido cálido para la <strong>ceremonia de boda</strong>, momento en que me gusta usar el Saxofón Soprano, hasta el sonido más potente y funky para una <strong>fiesta en Barra Libre de Boda</strong>.</p>

                                        <p>Queremos crear un <strong>ambiente único y especial</strong>, al gusto de la pareja que está dando uno de los pasos más importantes en su vida, dándoles todo el protagonismo y haciendo que el momento sea como uno llevaba años imaginándolo.</p>

                                        <blockquote>Cada boda es única, como la personalidad de cada una de las parejas, por eso me adapto a las peticiones de la pareja, con música a la carta y trabajando con vuestro DJ favorito.</blockquote>

                                        <p>¿Te imaginas tu boda en una <strong>iglesia</strong> del siglo XIII o en las <strong>playas</strong> de Cadaqués? ¿El banquete lo prefieres en los campos de una <strong>casa rural</strong> o en los salones de un <strong>castillo</strong>? Como decía, cada boda es un mundo, como las personalidades de las parejas, y así me adapto a vuestros gustos.</p>

