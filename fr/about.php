                            <h2 class="artist-name">Arnald SAXONARA</h2>
                            <p><b>Je suis Arnald SAXONARA, je suis musicien et saxophoniste lors d'événements, de mariages et aux concerts, ci-dessous je vous fais un bref résumé de mon parcours</b></p>
                            <p>Je suis né en 1990 👶 et j'ai commencé mes études musicales à l'âge de 7 ans, en jouant d'abord du <b>piano 🎹</b> et en suivant la formation formelle dans des Écoles de Musique. Au fil du temps, et à mesure que mes goûts évoluaient, j'ai mis de côté le piano pour me concentrer sur l'étude des <b>musiques modernes</b>, cette fois en tant que <b>guitariste 🎸</b> et déjà en participant à des groupes et en jouant en live. Quelques années plus tard, adolescent, j'ai commencé avec le <b>saxophone 🎷</b> comme deuxième instrument, mais il a rapidement pris le devant de la scène, reléguant la guitare au second plan. C'est avec le saxophone que j'étudie au <b>Conservatoire</b>.</p>
                            <p>Parallèlement à mes études musicales, j'ai également obtenu un diplôme en <b>Administration et Gestion des Affaires 📈</b> de l'Université de Lleida et du Centre College, Kentucky, <b>États-Unis 🇺🇸</b>, où j'ai étudié un mélange de matières dans l'enseignement libéral allant du Jazz et de la Musique traditionnelle à la Finance et à l'Économie. .</p>
                            <p>En 2013, je suis revenu <b>des États-Unis à l'Europe</b>, ce fut une période de réadaptation, où j'ai rejoint en tant que saxophoniste des groupes funk-disco, rockabilly et jazz, toujours impatient de jouer et d'apprendre de <b>différents styles</b>, et cherchant un moyen de présenter des projets lors de concerts et d’événements. A cette époque, je continue également à me former en tant que musicien dans des académies.</p>
                            <p>Déjà en 2015, j'ai déménagé à <b>Porto Rico 🇵🇷</b>, m'installant dans sa capitale, San Juan. C'est là que je commence à me familiariser avec toutes les musiques traditionnelles de l'archipel, notamment la Salsa, la Bomba, la Rumba et la Plena. À Porto Rico, j'ai formé mon groupe avec des musiciens du <b>Conservatoire de Musique de Porto Rico</b>, en me produisant dans des lieux aussi emblématiques que la scène du quartier de La Perla, dans le Vieux San Juan, la même scène où s'est produit le légendaire groupe portoricain Calle 13 après avoir été récompensé par des Latin Grammy Awards.</p>
                            <p>De retour de Porto Rico, et après une brève léthargie en <b>Argentine 🇦🇷</b>, je me suis réinstallé à l'Europe et <b>SAXONARA</b> est né, mon plus gros projet jusqu'à présent et auquel je consacre tout mon temps et mon énergie.</p>
