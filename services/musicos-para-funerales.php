                                        <p>El perder a un ser querido es un momento por el que nadie quiere pasar pero es inevitable amar a los nuestros y el que un día nos tengamos que despedir.</p>
                                        <p>El funeral de un ser querido puede ser un acto discreto o todo un homenaje y, al gusto de los seres queridos del difunto, podemos utilizar la música para que la <strong>misa, liturgia y/o funeral</strong> sea el deseado.</p>
                                        <p>En escoger la <strong>música para un funeral o velatorio</strong> nos podemos guiar por los deseos y/o gustos del difunto, interpretando partes de <b>sus canciones favoritas</b>, así como <b>música clásica popular</b> que bien seguro les habrá acompañado en algún momento de sus vidas. También se dispone de un <strong>amplio repertorio musical</strong> que es un recurso indispensable para momentos imprevistos como la despedida en un funeral.</p>

                                        <h3>Saxofonista para funeral</h3>
                                        <p>Como músico saxofonista utilizo mis saxofones y la flauta para interpretar las piezas solicitadas de la manera más sentida posible, rindiendo un homenaje y contribuyendo a ofrecer la despedida deseada.</p>
										<p>En mis interpretaciones de Solo Saxofón me acompaño, si la pieza lo requiere, de bases musicales compuestas en piano u otros instrumentos, que acompañan a mi interpretación en el <strong>velatorio o misa con saxofón</strong> en directo, logrando así una mejor armonización y rindiendo un homenaje más especial en el responso.</p>

