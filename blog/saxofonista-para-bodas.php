                                        <h3>La Fiesta o Barra Libre</h3>
                                        <p>Me considero un saxofonista versátil que disfruta trabajando en distintos ambientes, así, y también gracias a mis distintos saxofones, puedo econtrar el sonido más cálido para una <strong>ceremonia de boda</strong>, amenizar un cóctel y tocar junto a DJs en momentos como la Fiesta o Barra Libre, entreteniendo a los invitados con la música más divertida y moderna, así como los clásicos de la música disco, para no parar de bailar.</p>

                                        <p>He trabajado con muchísimos DJs a lo largo de mi carrera, por lo que me adaptaré a vuestro DJ en el día de la boda, así me pondré en contacto con anticipación, para preparar <strong>los mejores temas y la mejor mezcla</strong>, que os haga bailar y para un momento divertido. 

                                        <p>Para momentos de fiesta me valgo de saxofones con sonido potente y microfonía inalámbrica, para así estar <strong>en la pista de baile junto a los invitados</strong>, ofreciendo un mejor show.</p>

                                        <p>Adicionalmente también me sirvo de <strong>saxofones con luces LED</strong>, consiguiendo deslumbrar en las luces ténues de la pista de baile, un <strong>show impresionante y muy divertido</strong> que será de lo más original en tu boda.</p>

