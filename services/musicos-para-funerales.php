                                        <h3>Música para funeral - Selección musical</h3>
                                        <p>Un tiempo atrás la música para ceremonias de funeral estaba estandarizada y implicaba simplemente seleccionar de un listado de música que todos conocemos, la que en cierta manera muchas veces es música tradicional.</p>
										<p>Actualmente esto ha cambiado y podemos encontrar <b>ceremonias con música moderna</b>, con <b>baladas clásicas</b> del rock y, por supuesto, siguen habiendo los <b>clásicos de la música tradicional</b>.</p>
										<p>Como <strong>saxofonista profesional</strong> tengo un <strong>amplísimo repertorio de música</strong> en general, y además un listado de las piezas que más pueden acompañar en un funeral, pero además también la habilidad de aprender y poder interpretar de manera sentida piezas que no estén en el repertorio habitual, esta es la suerte de poder contar con un <b>músico profesional para rendir homenaje en un funeral</b>.</p>
