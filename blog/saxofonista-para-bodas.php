                                        <h3>El Aperitivo o Cóctel</h3>
                                        <p>Los <strong>jardines en el exterior</strong> son una gran opción para el aperitivo o <strong>cóctel de bienvenida</strong>, como saxofonista buscaré crear un <strong>ambiente especial</strong> mientras los invitados van llegando y encontrándose. La mejor opción para este momento es una ámplia selección de temas comerciales y conocidos, música House y incluso temas más jazzy.</p>

                                        <p>Como saxofonista me gusta la libertad que me da el tocar en solitario y la posibilidad de interactuar con los invitados, ofreciéndoles mi <strong>música a la carta</strong>, para que puedan escoger sobre la marcha que canciones escuchar en saxofón.</p>

